function search() {
    var searchMovie = document.getElementsByTagName('input')[0].value;

    if(!searchMovie){
        var movies = allMovies;
    } else {
        var movies = allMovies.filter((movie) => {
            return movie.name.includes(searchMovie) || movie.director.includes(searchMovie);
        })
    }
    var moviesHtml = '';

    movies.forEach((movie) => {

        moviesHtml += `<div class="col-sm-4">
                <div class="card" style="width: 18rem;">
                    <img src=${movie.image_url} class="card-img-top" alt="...">
                    <div class="card-body">
                        <h4 class="card-title">${movie.name}</h4>
                        <ul>
                            <li><strong>کارگردان: </strong> ${movie.director}</li>
                            <li><strong>مدت فیلم (دقیقه): </strong> ${movie.duration}</li>
                            <li><strong>ژانر: </strong> ${movie.genre}</li>
                            <li><strong>توضیحات: </strong> ${movie.description}</li>
                        </ul>
                    </div>
                </div>
             </div>`
    });

    document.getElementById('movies').innerHTML = moviesHtml;
}

search()